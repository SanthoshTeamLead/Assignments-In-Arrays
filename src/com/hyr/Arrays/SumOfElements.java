package com.hyr.Arrays;

public class SumOfElements {

	public static void main(String[] args) {
		int[][] sumElements  = new int[3][3];
		sumElements[0] = new int[] {1,8,4};
		sumElements[1] = new int[] {9,7,2};
		sumElements[2] = new int[] {7,6,4};
		
		int totalRows  = sumElements.length;
		System.out.println(totalRows);
	}

}
